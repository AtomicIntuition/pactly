INSERT INTO storage.buckets (id, name, public) VALUES ('logos', 'logos', true);

-- Users upload/update/delete their own folder
CREATE POLICY "Users can upload own logo" ON storage.objects FOR INSERT TO authenticated
  WITH CHECK (bucket_id = 'logos' AND (storage.foldername(name))[1] = auth.uid()::text);

CREATE POLICY "Users can update own logo" ON storage.objects FOR UPDATE TO authenticated
  USING (bucket_id = 'logos' AND (storage.foldername(name))[1] = auth.uid()::text);

CREATE POLICY "Users can delete own logo" ON storage.objects FOR DELETE TO authenticated
  USING (bucket_id = 'logos' AND (storage.foldername(name))[1] = auth.uid()::text);

-- Public read (needed for PDF renderer + unauthenticated share page)
CREATE POLICY "Anyone can read logos" ON storage.objects FOR SELECT TO public
  USING (bucket_id = 'logos');
